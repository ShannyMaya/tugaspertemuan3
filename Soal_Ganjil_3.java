public class Java3 {
    public static void main(String[] args) {
        char karakter1 = '1';
        char karakter2 = '2';
        
        int ascii1 = (int) karakter1;
        int ascii2 = (int) karakter2;
        int sum = ascii1 + ascii2;
        
        System.out.println("Nilai ASCII dari '1' adalah " + ascii1);
        System.out.println("Nilai ASCII dari '2' adalah " + ascii2);
        System.out.println("Jumlah nilai ASCII adalah " + sum);
    }
}
